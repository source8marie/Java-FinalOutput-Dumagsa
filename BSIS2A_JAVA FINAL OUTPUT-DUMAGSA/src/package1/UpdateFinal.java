    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
    
        try {
        pst = con.prepareStatement("select Id,fname,lname,address, mobileno,email, password from reg where Id=? ");
        int id = Integer.parseInt(searchId.getText());
        pst.setInt(1, id);
        ResultSet rs1 = pst.executeQuery();

        if (rs1.next() == false) {
            JOptionPane.showMessageDialog(this, "Sorry Record Not Found");
            textName.setText("");
            textLast.setText("");
            textAddress.setText("");
            textMobileNo.setText("");
            textEmail.setText("");
            textPass.setText("");

            searchId.requestFocus();
            // Disable all fields except searchId
            enableFields(false);
        } else {
            textName.setText(rs1.getString("fname"));
            textLast.setText(rs1.getString("lname"));
            textAddress.setText(rs1.getString("address"));
            textMobileNo.setText(rs1.getString("mobileno"));
            textEmail.setText(rs1.getString("email"));
            textPass.setText(rs1.getString("password"));

            // Enable all fields for editing
            enableFields(true);

            editButton.setEnabled(true);
        }
    } catch (SQLException ex) {
        // Handle exception
        ex.printStackTrace();
    }
    }//GEN-LAST:event_searchButtonActionPerformed

