    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
    
    int answer = JOptionPane.showConfirmDialog(null, "Are you sure you want to update this record?", "System Message", JOptionPane.YES_NO_OPTION);

    if (answer == JOptionPane.YES_OPTION) {
        // Validate contact number
        String mobileNo = textMobileNo.getText();
        if (mobileNo.length() != 11) {
            JOptionPane.showMessageDialog(this, "Contact number must be exactly 11 digits long.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        // Validate password
        String password = new String(textPass.getPassword());
        if (password.length() < 5) {
            JOptionPane.showMessageDialog(this, "Password must be at least 5 characters long.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        try {
            deleteButton.setEnabled(false);
            Connection cn;
            Statement st;
            String url;
            url = "jdbc:mysql://localhost:3306/db";
            Class.forName("com.mysql.cj.jdbc.Driver");
            cn = (Connection) DriverManager.getConnection(url, "root", "buhaycolegio@2024");
            st = (Statement) cn.createStatement();
            st.executeUpdate("UPDATE reg SET fname='"
                    + textName.getText() + "', lname='"
                    + textLast.getText() + "', address='"
                    + textAddress.getText() + "', mobileno='"
                    + textMobileNo.getText() + "', email='"
                    + textEmail.getText() + "', password='"
                    + textPass.getText()
                    + "' Where Id='"
                    + searchId.getText() + "'");

            JOptionPane.showMessageDialog(null, "Record Updated", "System Message", JOptionPane.INFORMATION_MESSAGE);

            textName.setText("");
            textLast.setText("");
            textAddress.setText("");
            textMobileNo.setText("");
            textEmail.setEnabled(false);
            textPass.setText("");

            Refresh();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
            // Handle class not found exception
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Update failed. Please check the data and try again.", "System Message", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
       int answer = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "System Message", JOptionPane.YES_NO_OPTION);

    if (answer == JOptionPane.YES_OPTION) {
        try {
            Connection cn;
            Statement st;
            String url;

            url = "jdbc:mysql://localhost:3306/db";
            cn = (Connection) DriverManager.getConnection(url, "root", "buhaycolegio@2024");
            st = (Statement) cn.createStatement();

            // Delete the record
            String query = "DELETE FROM reg WHERE id = ?";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setInt(1, Integer.parseInt(searchId.getText()));
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Record Deleted", "System Message", JOptionPane.INFORMATION_MESSAGE);

            // Clear the fields
            textName.setText("");
            textLast.setText("");
            textAddress.setText("");
            textMobileNo.setText("");
            textEmail.setText("");
            textPass.setText("");

            // Disable all fields except searchId
            enableFields(false);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Deletion failed. Please try again.", "System Message", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

